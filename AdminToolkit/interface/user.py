####################################################################################################

__all__ = ['is_root', 'raise_if_not_root']

####################################################################################################

import getpass

####################################################################################################

def is_root() -> bool:
    user = getpass.getuser()
    # print(user)
    return user == 'root'

####################################################################################################

def raise_if_not_root() -> None:
    if not is_root():
        raise NameError('Root privileges are required')
